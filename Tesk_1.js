// 자바스크립트 변수와 연습자 연습
/*
변수를 선언하고 값을 할당한 후, 
기본 연산자(+,-,*,/)를 사용하여 간단한 계산을 수행합니다. 
예를 들어, 두 숫자를 더한 결과를 출력하거나 나누기를 사용하여 나머지를 계산해 봅니다.
결과물로 코드와 콘솔 출력 스크린샷을 제출합니다.
*/

let num1 = 10;
let num2 = 20;
let num3 = 30;
let num4 = 40;


console.log(`num1 + num2 = ${num1 + num2}`);
console.log(`num3 - num2 = ${num3 - num2}`);
console.log(`num1 * num3 = ${num1 * num2}`);
console.log(`num4 / num2 = ${num4 / num2}`);